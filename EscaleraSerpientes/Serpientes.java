
package EscaleraSerpientes;

public class Serpientes {
    private int inicio;
    private int fin;

    public Serpientes() {
    }
    
    

    public Serpientes(int inicio, int fin) {
        this.inicio = inicio;
        this.fin = fin;
    }

    public int getInicio() {
        return inicio;
    }

    public void setInicio(int inicio) {
        this.inicio = inicio;
    }

    public int getFin() {
        return fin;
    }

    public void setFin(int fin) {
        this.fin = fin;
    }
}
